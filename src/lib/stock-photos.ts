export type PhotoCategory = keyof typeof STOCK_PHOTOS;

/**
 * Get a deterministic photo by category and index.
 * Use this for galleries, testimonials, etc. where order should be stable.
 * @param category - Photo category (food, avatar, tech, etc.)
 * @param index - Index of the photo (0, 1, 2, ...)
 */
export function getPhoto(category: PhotoCategory | string, index: number = 0): string {
    const keys = Object.keys(STOCK_PHOTOS) as Array<keyof typeof STOCK_PHOTOS>;
    const safeCategory = keys.includes(category as any) ? category as keyof typeof STOCK_PHOTOS : 'abstract';
    
    const photos = STOCK_PHOTOS[safeCategory];
    // Use modulo to cycle through available photos
    const safeIndex = Math.abs(index) % photos.length;
    return photos[safeIndex];
}

/**
 * Get a random photo from a category.
 * NOTE: This returns DIFFERENT photo on each call/refresh!
 * For stable galleries, use getPhoto(category, index) instead.
 */
export function getRandomPhoto(category: PhotoCategory | string): string {
    const keys = Object.keys(STOCK_PHOTOS) as Array<keyof typeof STOCK_PHOTOS>;
    const safeCategory = keys.includes(category as any) ? category as keyof typeof STOCK_PHOTOS : 'abstract';
    
    const photos = STOCK_PHOTOS[safeCategory];
    return photos[Math.floor(Math.random() * photos.length)];
}
